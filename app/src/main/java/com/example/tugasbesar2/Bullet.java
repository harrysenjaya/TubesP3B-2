package com.example.tugasbesar2;

public class Bullet {
    private int x;
    private int y;
    private int width;


    public Bullet(int x, int y){
        this.x = x;
        this.y = y;
    }

    public int getX(){
        return this.x;
    }

    public int getY(){
        return this.y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
